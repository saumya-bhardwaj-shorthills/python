# Assignment statement
x = 10  
print("value of x is : ", x)

#control flow statement
if x > 5:  # This is a statement that checks a condition
    print("x is greater than 5")  # This print statement performs an action

# Loop statement
for i in range(5):  # This statement initiates a loop
    print(i)  # This print statement performs an action in each iteration
